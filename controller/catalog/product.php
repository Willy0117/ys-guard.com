	protected function validateForm() {
		if (!$this->user->hasPermission('modify', 'catalog/product')) {
			$this->error['warning'] = $this->language->get('error_permission');
		}

		if ((utf8_strlen($this->request->post['name']) < 1) || (utf8_strlen($this->request->post['name']) > 255)) {
			$this->error['name'] = $this->language->get('error_name');
		}

		if ((utf8_strlen($this->request->post['model']) < 1) || (utf8_strlen($this->request->post['model']) > 64)) {
			$this->error['model'] = $this->language->get('error_model');
		}
		if (!isset($this->request->post['category_id']) or $this->request->post['category_id'] == 0) {
			$this->error['category'] = $this->language->get('error_category');
		}
/* 得意先は必須にしない　2017.09.12
		if (!isset($this->request->post['customer_group']) or $this->request->post['customer_group'] == 0) {
			$this->error['customer_group'] = $this->language->get('error_customer_group');
		}
*/
		if ($this->error && !isset($this->error['warning'])) {
			$this->error['warning'] = $this->language->get('error_warning');
		}

		return !$this->error;
	}

