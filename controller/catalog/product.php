			$category = '';
			foreach($data['categoryes'] as $resu) {
				if ($resu['category_id'] == $result['category_id']) $category = $resu['name'];
			}
			$customer_group = '';
			foreach($data['customer_groups'] as $resu) {
				if ($resu['id'] == $result['customer_group']) $customer_group = $resu['name'];
			}

			$data['products'][] = array(
				'id'			 => $result['id'],
				'customer_group' => $customer_group,
				'category' 		 => $category,
				'name'      	 => $result['name'],
				'price'			 => '',
				'model'     	 => $result['model'],
				'quantity' 		 => $result['quantity'],
				'status'    	 => ($result['status']) ? $this->language->get('text_enabled') : $this->language->get('text_disabled'),
				'edit'      	 => $this->url->link('catalog/product/edit', 'token=' . $this->session->data['token'] . '&id=' . $result['id'] . $url, 'SSL')
